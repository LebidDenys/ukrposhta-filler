const CODE_INDEX = new Map<string, IsoCountry>(
  ISO_COUNTRIES.map((c) => [c.code.toUpperCase(), c]),
);

const NAME_INDEX = new Map<string, IsoCountry>();
for (const c of ISO_COUNTRIES) {
  NAME_INDEX.set(normalizeName(c.en), c);
  NAME_INDEX.set(normalizeName(c.uk), c);
  if (c.aliases) {
    for (const a of c.aliases) NAME_INDEX.set(normalizeName(a), c);
  }
}

function normalizeName(s: string): string {
  return s.toLowerCase().replace(/[.\s'’\-]+/g, "");
}

const CODE_RE = /(?<![A-Za-z])[A-Z]{2}(?![A-Za-z])/g;
const NAME_RE = new RegExp(
  `\\b(${ISO_COUNTRIES.flatMap((c) =>
    [c.en, c.uk, ...(c.aliases ?? [])].map((n) =>
      n.replace(/[\\^$.*+?()[\]{}|]/g, "\\$&"),
    ),
  )
    .sort((a, b) => b.length - a.length)
    .join("|")})\\b`,
  "i",
);

export function getCountryByCode(code: string): IsoCountry | undefined {
  return CODE_INDEX.get(code.toUpperCase());
}

export function getCountryByName(name: string): IsoCountry | undefined {
  return NAME_INDEX.get(normalizeName(name));
}

export interface CountryMatch {
  country: IsoCountry;
  matchedText: string;
}

export function findCountry(text: string): CountryMatch | undefined {
  const nameMatch = text.match(NAME_RE);
  if (nameMatch && nameMatch[1] !== undefined) {
    const c = getCountryByName(nameMatch[1]);
    if (c) return { country: c, matchedText: nameMatch[1] };
  }
  CODE_RE.lastIndex = 0;
  let m: RegExpExecArray | null;
  while ((m = CODE_RE.exec(text)) !== null) {
    const c = getCountryByCode(m[0]);
    if (c) return { country: c, matchedText: m[0] };
  }
  return undefined;
}
